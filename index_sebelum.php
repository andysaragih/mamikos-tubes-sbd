            <p class="article4">Mamikos - Aplikasi Anak Kos No. 1 di Indonesia</p>
            <br>
            <p class="article5">Mamikos memanfaatkan teknologi untuk berkembang dari aplikasi cari kos menjadi aplikasi yang memudahkan calon anak kos untuk booking properti kos dan juga melakukan pembayaran kos.
              Saat ini kami memiliki lebih dari 2 juta kamar kos yang tersebar di lebih dari 140 kota di seluruh Indonesia. Mamikos juga menyediakan layanan manajemen properti, bernama Singgahsini dan Apik,
              untuk menjawab kebutuhan calon penghuni yang menginginkan kos eksklusif atau kos murah. Mamikos berusaha untuk bisa terus menyajikan daftar rumah kos dengan data ketersediaan kamar yang akurat,
              fasilitas kos terperinci, dilengkapi dengan foto serta detail harga kos, dan kemudahan survei via fitur virtual tour agar calon penghuni mendapatkan kenyamanan dalam proses pencarian dan booking kos.

              <br>
              <br>
              Untuk memberikan perlindungan bagi para anak kos selama pandemi, Mamikos menghadirkan
              <strong>Kos Higienis</strong>
              . Kos Higienis merupakan kos dengan konsep yang menerapkan pelaksanaan standar protokol kesehatan, seperti disinfeksi kamar, jaga jarak, penggunaan masker, dan swab antigen untuk kamu saat akan mulai ngekos nanti.
